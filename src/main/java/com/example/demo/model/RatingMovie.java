package com.example.demo.model;

import jakarta.persistence.*;
import jakarta.validation.constraints.Size;


@Entity
@Table(name = "RatingMovie", catalog = "CinestarDatabase", schema = "dbo")

public class RatingMovie {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id", nullable = false)
    private Integer id;
    @Column(name = "rating")
    private Integer rating;
    @Size(max = 250)
    @Column(name = "user_rating", length = 250)
    private String userRating;
    @JoinColumn(name = "movie_id", referencedColumnName = "movieId")
    @ManyToOne
    private Movie movieId;

    public RatingMovie() {
    }

    public RatingMovie(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getRating() {
        return rating;
    }

    public void setRating(Integer rating) {
        this.rating = rating;
    }

    public String getUserRating() {
        return userRating;
    }

    public void setUserRating(String userRating) {
        this.userRating = userRating;
    }

    public Movie getMovieId() {
        return movieId;
    }

    public void setMovieId(Movie movieId) {
        this.movieId = movieId;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof RatingMovie)) {
            return false;
        }
        RatingMovie other = (RatingMovie) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "models.RatingMovie[ id=" + id + " ]";
    }
    
}
